async function seedValues() {
  if (!process.env.DATABASE_URL) {
    console.error("DATABASE_URL is not set");
    process.exit(1);
  }

  const db = drizzle(process.env.DATABASE_URL);

  console.log("Seeding default values...");

  const valuesToInsert = defaultValues.map(name => ({
    id: nanoid(),
    name,
    isDefault: true,
    createdBy: null,
  }));

  await db.insert(coreValues).values(valuesToInsert);

  console.log(`✓ Inserted ${valuesToInsert.length} default values`);
  process.exit(0);
}

seedValues().catch((error) => {
  console.error("Error seeding values:", error);
  process.exit(1);
});

